package InheritanceDemos;

public class OverloadingDemo2 {


    public static void main(String[] args) {

        // println() - is a method

        System.out.println("my name is amol"); // string as an argument

        int a = 10 ;

        System.out.println(a); // int as an argument

        double d = 2.2;

        System.out.println(d); // double as an argument

        boolean result = true;

        System.out.println(result); // boolean as an argument

        char c = 'f';

        System.out.println(c); // character as an argument


    }


}
